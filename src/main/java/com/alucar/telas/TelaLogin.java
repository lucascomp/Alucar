    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String login = textoLogin.getText();
        String senha = textoSenha.getText();
        
        if(login.length() != 5 || senha.length() < 5) {
            JOptionPane.showMessageDialog(this, "Login ou senha inválidos.");
            textoLogin.setText("");
            textoSenha.setText("");
        }
        else {
            Associado associado;
            DaoAssociado daoAssociado = new DaoAssociado();
            associado = daoAssociado.logar(login, senha);
            if(associado != null) {
                JOptionPane.showMessageDialog(this, "Login efetuado com sucesso!");
                TelaLogado tela = new TelaLogado(associado);
                tela.setVisible(true);
                dispose();
            }
            else {
                JOptionPane.showMessageDialog(this, "Login ou senha inválidos.");
                textoLogin.setText("");
                textoSenha.setText("");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

